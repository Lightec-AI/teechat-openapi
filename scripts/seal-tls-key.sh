#!/usr/bin/env bash
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "${ROOT}"

if [[ $# -ne 2 ]]; then
  echo "Usage: OPENAPI_LAUNCH_DIGEST=... OPENAPI_IMAGE_DIGEST=... [OPENAPI_SEAL_ROOT_HEX=...] $0 <plain-key.pem> <sealed-out.json>" >&2
  exit 1
fi

export OPENAPI_UPSTREAM_BASE_URL="${OPENAPI_UPSTREAM_BASE_URL:-http://127.0.0.1:1}"
export OPENAPI_CATALOG_PATH="${OPENAPI_CATALOG_PATH:-${ROOT}/dev/catalog.json}"
export OPENAPI_CATALOG_VERIFY_KEY_HEX="${OPENAPI_CATALOG_VERIFY_KEY_HEX:-0101010101010101010101010101010101010101010101010101010101010101}"
export OPENAPI_USAGE_SIGN_SEED_HEX="${OPENAPI_USAGE_SIGN_SEED_HEX:-0202020202020202020202020202020202020202020202020202020202020202}"
export OPENAPI_LAUNCH_DIGEST="${OPENAPI_LAUNCH_DIGEST:-unknown}"
export OPENAPI_IMAGE_DIGEST="${OPENAPI_IMAGE_DIGEST:-unknown}"

cargo run -q -p seal-tls-key -- "$1" "$2"
